<template>
  <div id="app">
    <router-view v-if="isRouterAlive"></router-view>
  </div>
</template>

<script>

export default {
  name: 'app',
  data(){
    return{
      isRouterAlive:true
    }
  },
  // 广播机制
  provide(){
    return {
      reload:this.reload
    }
  },
  methods:{
    reload(){
      this.isRouterAlive = false;
      this.$nextTick(() =>this.isRouterAlive = true)
    }
  }
}
</script>
